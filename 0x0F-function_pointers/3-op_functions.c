#include "3-calc.h"
#include <stdlib.h>
#include <stddef.h>

/**
 * op_add - function that adds integers
 * @a: integer
 * @b: integer
 * Return: void
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - function that subtracts integers
 * @a: integer
 * @b: integer
 * Return: void
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - function that multiples integers
 * @a: integer
 * @b: integer
 * Return: void
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - function that divides integers
 * @a: integer
 * @b: integer
 * Return: void
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - function that gives the remainder integers
 * @a: integer
 * @b: integer
 * Return: void
 */
int op_mod(int a, int b)
{
return (a % b);
}
