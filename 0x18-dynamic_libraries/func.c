#include <stdio.h>

/**
 * add - funtion that adds
 * @x: integer
 * @y: integer
 * Return: void
 */

int add(int x, int y)
{
return (x + y);
}
/**
 * sub - function that subtracts
 * @x: integer
 * @y: integer
 * Return: void
 */
int sub(int x, int y)
{
return (x - y);
}
/**
 * mul - function that multiples
 * @x: integer
 * @y: integer
 * Return: void
 */
int mul(int x, int y)
{
return (x * y);
}
/**
 * div - function that divides
 * @x: integer
 * @y: integer
 * Return: void
 */
int div(int x, int y)
{
return (x / y);
}
/**
 * mod - function that mod
 * @x: integer
 * @y: integer
 * Return: void
 */
int mod(int x, int y)
{
return (x % y);
}
